<h2 name="letter">Как редактировать шаблон письма</h2>
<div class="answer">
	<p>1. заходи на хостинг /mail/views</p>
	<p>2. каждый шаблон имеет 2 версии html и text (для старых устройств)</p>
	<p>- reg = шаблон письма регистрации</p>
	<p>- ticketbuy = шаблон письма покупки билета</p>
</div>


<h2 name="letter2">В дате события нет билетов или рассадки</h2>
<div class="answer">
	<p>Удалите дату и создайте ее по-новой.</p>
	<p>Если это не поможет, удалите само события и создайте его и его рассадку по-новой.</p>
</div>



<a name="biblioevents"></a>
<h2>Как создать событие</h2>
<div class="answer">
	<p class="col-xs-12 col-lg-2"><a href="/places/create" class="btn btn-primary" target="_blank">Создайте площадку</a></p>
	<p class="col-xs-12 col-lg-10">Это заведение (место), в котором будет проходить ваше событие. Площадка нужна для создания календаря событий, и чтобы организатор мог знать когда есть свободное время, а когда площадка занята.</p>
	<div class="clear"></div>

	<p class="col-xs-12 col-lg-2"><a href="/seatings/create" class="btn btn-primary" target="_blank">Создайте рассадку</a></p>
	<p class="col-xs-12 col-lg-10">Рассадка это количество и стоимость мест на вашем событии.</p>
	<div class="clear"></div>

	<p class="col-xs-12 col-lg-2"><a href="/seats/create" class="btn btn-primary" target="_blank">Создайте типы билетов</a></p>
	<p class="col-xs-12 col-lg-10">Билеты нужны, чтобы не продать мест больше чем возможно и разделить места по цене, например первый ряд, или vip зона.<br>В версии CRM 2.0 билеты не привязаны к каким-то конкретным рядам, вы просто называете группу мест, устанавливаете их количество и стоимость.</p>
	<div class="clear"></div>

	<p class="col-xs-12 col-lg-2"><a href="/categoryevents/create" class="btn btn-primary" target="_blank">Создайте категорию</a></
	p>
	<p class="col-xs-12 col-lg-10">Как правило категорию создавать не нужно, уже есть категория для вашего события. Если это так, переходите к следующему шагу.</p>
	<div class="clear"></div>

	<p class="col-xs-12 col-lg-2"><a href="/biblioevents/create" class="btn btn-primary" target="_blank">Создайте событие</a></p>
	<p class="col-xs-12 col-lg-10">Теперь создайте событие, и укажите в нем рассадку, а типы билетов подхватятся автоматически. Но событию вы не назначаете дату, потому что у вашего события может быть несколько дат. Для этого переходим к созданию даты.</p>
	<div class="clear"></div>

	<p class="col-xs-12 col-lg-2"><a href="/events/create" class="btn btn-primary" target="_blank">Создайте дату</a></p>
	<p class="col-xs-12 col-lg-10">Выберите из списка ваше событие и дату, вся остальная информация заполнится автоматически.</p>
</div>

